package hexlet.code.Differ;

import hexlet.code.Parser.Parser;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import static org.assertj.core.api.Assertions.assertThat;

class TestDiffer {
    private Parser parser;

    @BeforeEach
    private void prepare() {
        parser = new Parser();
    }

    @Test
    void testDiffJson() throws Exception {
        String filepath1 = "file1.json";
        String filepath2 = "file2.json";
        String actual1 = Differ.generate(parser.getData(filepath1), parser.getData(filepath2));
        String expected1 = """
                {
                    chars1: [a, b, c]
                  - chars2: [d, e, f]
                  + chars2: false
                  - checked: false
                  + checked: true
                  - default: null
                  + default: [value1, value2]
                  - id: 45
                  + id: null
                  - key1: value1
                  + key2: value2
                    numbers1: [1, 2, 3, 4]
                  - numbers2: [2, 3, 4, 5]
                  + numbers2: [22, 33, 44, 55]
                  - numbers3: [3, 4, 5]
                  + numbers4: [4, 5, 6]
                  + obj1: {nestedKey=value, isNested=true}
                  - setting1: Some value
                  + setting1: Another value
                  - setting2: 200
                  + setting2: 300
                  - setting3: true
                  + setting3: none
                }""";
        assertThat(actual1).isEqualTo(expected1);
    }

    @Test
    void testDiffYaml() throws  Exception {
        String filepath1 = "file1.yml";
        String filepath2 = "file2.yml";
        String expected1 = """
                {
                    chars1: [a, b, c]
                  - chars2: [d, e, f]
                  + chars2: false
                  - checked: false
                  + checked: true
                  - default: null
                  + default: [value1, value2]
                  - id: 45
                  + id: null
                  - key1: value1
                  + key2: value2
                    numbers1: [1, 2, 3, 4]
                  - numbers2: [2, 3, 4, 5]
                  + numbers2: [22, 33, 44, 55]
                  - numbers3: [3, 4, 5]
                  + numbers4: [4, 5, 6]
                  + obj1: {nestedKey=value, isNested=true}
                  - setting1: Some value
                  + setting1: Another value
                  - setting2: 200
                  + setting2: 300
                  - setting3: true
                  + setting3: none
                }""";
        String actual1 = Differ.generate(parser.getData(filepath1), parser.getData(filepath2));
        assertThat(expected1).isEqualTo(actual1);
    }
}
